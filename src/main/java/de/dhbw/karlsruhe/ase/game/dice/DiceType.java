package de.dhbw.karlsruhe.ase.game.dice;

/**
 * A dice type specifying the number of sides the dice has
 *
 * @author Dominik Ochs
 * @version 1.0
 */
public enum DiceType {
    /**
     * A four sided dice
     */
    FOUR_SIDED,
    /**
     * A six sided dice
     */
    SIX_SIDED,
    /**
     * An eight sided dice
     */
    EIGHT_SIDED
}
